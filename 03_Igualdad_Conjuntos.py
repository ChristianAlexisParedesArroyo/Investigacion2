#En esta practica se muestran la igualdad de conjuntos


A = {1, 2, 4, 5}  #se crea un conjunto A
B = {1, 2, 5, 4}  #se crea un conjunto B
C = {3, 2, 7, 4}  #se crea un conjunto C

print(A)
print(B)
print(C)

if A == B:  #se comparan los conjuntos A y B
    print("El conjunto A y B son iguales")  #si son iguales se imprime este mensaje
else:
    print("El conjunto A y B son diferentes")  #si son diferentes se imprime este mensaje

if A == C:  #se comparan los conjuntos A y C
    print("El conjunto A y C son iguales")  #si son iguales se imprime este mensaje
else:
    print("El conjunto A y C son diferentes")  #si son diferentes se imprime este mensaje
